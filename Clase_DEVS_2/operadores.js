
// Un Operador es un simbolo que realiza una operacion sobre uno o mas valores y produce un resultado.

// Operadores Aritmeticos
let num1 = 10;
let num2 = 5;

console.log("Suma: ", (num1 + num2));
console.log("Resta: ", (num1 - num2));
console.log("Multiplicacion: ", (num1 * num2));
console.log("Division: ", (num1 / num2));

// Operadores de Comparacion

const prioridadA = 5;
const prioridadB = 10;

console.log(prioridadA < prioridadB);
console.log(prioridadA > prioridadB);
console.log(prioridadA === prioridadB);
console.log(prioridadA == prioridadB);


// Operadores Logicos
const prioridadAlta=true;
const Completada= false;

console.log(prioridadAlta && Completada);
console.log(prioridadAlta || Completada);
